# python tiene varios tipos de datos
# int, float, complex, str, bool, list, NoneType, list, tuple, dict, range, set
###

print (type(43)) # int
print (type(43.0)) # float
print (type(43j)) # complex
print (type("Hola")) # str
print (type(True)) # bool
print (type([1,2,3])) # list
print (type(None)) # NoneType
print (type((1,2,3))) # tuple
print (type({"a":1, "b":2})) # dict
print (type(range(10))) # range
print (type({1,2,3})) # set